

####  Exercicio 01 

# 1. Criar variáveis
a <- 12
b <- 5

# 2. Operações aritméticas
soma <- a + b          # 17
subtracao <- a - b     # 7
divisao_inteira <- a %/% b   # 2
resto <- a %% b        # 2

# 3. Comparação
maior_que <- a > b     # TRUE

# 4. Operador lógico
teste_logico <- a > 10 & b < 10   # TRUE

# 5. Vetor e verificação
vetor <- 1:10
verifica_7 <- 7 %in% vetor   # TRUE

# Mostrar resultados
soma; subtracao; divisao_inteira; resto
maior_que
teste_logico
verifica_7


